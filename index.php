<script>
  document.addEventListener('DOMContentLoaded', function() {
    const modal = document.getElementById('Modal');
    const allowBtn = document.getElementById('allowBtn');
    const laterBtn = document.getElementById('laterBtn');

    // Function to display the modal
    function showModal() {
        modal.style.display = 'block';
    }

    // Function to hide the modal
    function hideModal() {
        modal.style.display = 'none';
    }

    // Check if the user has previously allowed the modal
    if (!localStorage.getItem('modalAllowed')) {
        // Show the modal on page load if not previously allowed
        showModal();
    }

    // Handle click on 'Allow' button
    allowBtn.addEventListener('click', function() {
        // Set flag in localStorage
        localStorage.setItem('modalAllowed', 'true');
        // Hide the modal
        hideModal();
    });

    // Handle click on 'Later' button
    laterBtn.addEventListener('click', function() {
        // Optionally handle the "Later" button action here
        // Hide the modal
        hideModal();
    });

    // Optional: Close the modal when clicking outside of the modal-content
    window.addEventListener('click', function(event) {
        if (event.target === modal) {
            hideModal();
        }
    });
  });

</script>
